using System.Text;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.IdentityModel.Tokens;
using Microsoft.OpenApi.Models;
using Prometheus;
using Serilog;
using VehicleExpense.Api.Endpoints;
using VehicleExpense.Api.Middleware;
using VehicleExpense.Infrastructure.DependencyInjection;

// ?? Serilog bootstrap ????????????????????????????????????????????????????????
Log.Logger = new LoggerConfiguration()
    .ReadFrom.Configuration(new ConfigurationBuilder()
        .AddJsonFile("appsettings.json")
        .Build())
    .Enrich.FromLogContext()
    .CreateLogger();

var builder = WebApplication.CreateBuilder(args);
builder.Host.UseSerilog();

// ?? Infrastructure & Application services ???????????????????????????????????
builder.Services.AddInfrastructure(builder.Configuration);

// ?? CORS Policy for Angular UI ??????????????????????????????????????????????
builder.Services.AddCors(options =>
{
    options.AddPolicy("AngularApp", policy =>
    {
        policy.WithOrigins(
                "http://localhost:4200",
                "https://localhost:4200",
                "http://localhost:5173",  // Vite dev server
                "https://localhost:5173"
            )
            .AllowAnyMethod()
            .AllowAnyHeader()
            .AllowCredentials()
            .WithExposedHeaders("Content-Disposition"); // For file downloads
    });
});

// ?? JWT Authentication ???????????????????????????????????????????????????????
var jwtSection = builder.Configuration.GetSection("Jwt");
var secret = jwtSection["Secret"] ?? throw new InvalidOperationException("JWT Secret missing.");
builder.Services.AddAuthentication(JwtBearerDefaults.AuthenticationScheme)
    .AddJwtBearer(options =>
    {
        options.TokenValidationParameters = new TokenValidationParameters
        {
            ValidateIssuer = true,
            ValidateAudience = true,
            ValidateLifetime = true,
            ValidateIssuerSigningKey = true,
            ValidIssuer = jwtSection["Issuer"],
            ValidAudience = jwtSection["Audience"],
            IssuerSigningKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(secret))
        };
    });
builder.Services.AddAuthorization();

// ?? Swagger / OpenAPI ????????????????????????????????????????????????????????
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(c =>
{
    c.SwaggerDoc("v1", new OpenApiInfo
    {
        Title = "VehicleExpense API",
        Version = "v1",
        Description = "Track vehicle fuel and maintenance expenses."
    });

    // Allow sending JWT from Swagger UI
    var jwtSecurityScheme = new OpenApiSecurityScheme
    {
        Name = "Authorization",
        Description = "Enter 'Bearer {token}'",
        In = ParameterLocation.Header,
        Type = SecuritySchemeType.ApiKey,
        Scheme = JwtBearerDefaults.AuthenticationScheme,
        Reference = new OpenApiReference
        {
            Type = ReferenceType.SecurityScheme,
            Id = JwtBearerDefaults.AuthenticationScheme
        }
    };
    c.AddSecurityDefinition(JwtBearerDefaults.AuthenticationScheme, jwtSecurityScheme);
    c.AddSecurityRequirement(new OpenApiSecurityRequirement
    {
        { jwtSecurityScheme, Array.Empty<string>() }
    });
});

// ?? Health Checks ????????????????????????????????????????????????????????????
builder.Services.AddHealthChecks();

var app = builder.Build();

// ?? Middleware pipeline ??????????????????????????????????????????????????????
app.UseMiddleware<ExceptionMiddleware>();
app.UseMiddleware<RequestLoggingMiddleware>();

// ?? CORS - must be before Authentication/Authorization ??????????????
app.UseCors("AngularApp");

if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
    app.MapGet("/", () => Results.Redirect("/swagger")).ExcludeFromDescription();
}

app.UseAuthentication();
app.UseAuthorization();

// ?? Prometheus metrics ???????????????????????????????????????????????????????
app.UseHttpMetrics();
app.MapMetrics("/metrics");

// ?? Health endpoint ??????????????????????????????????????????????????????????
app.MapHealthChecks("/health");

// ?? API Endpoints ????????????????????????????????????????????????????????????
app.MapAuthEndpoints();
app.MapUserEndpoints();
app.MapVehicleEndpoints();
app.MapFuelEndpoints();
app.MapExpenseEndpoints();
app.MapDashboardEndpoints();

app.Run();

