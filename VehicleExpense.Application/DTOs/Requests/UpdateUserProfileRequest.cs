using System.ComponentModel.DataAnnotations;
using VehicleExpense.Domain.Enums;

namespace VehicleExpense.Application.DTOs.Requests;

/// <summary>
/// Request payload for updating a user's profile.
/// Required fields are safety-critical (emergency contact, blood group, mobile, date of birth).
/// All other fields are optional.
/// </summary>
public record UpdateUserProfileRequest
{
    // ?? Optional identity fields ??????????????????????????????????????????????

    [MaxLength(100)]
    public string? FullName { get; init; }

    [MaxLength(2048)]
    public string? ProfileImageUrl { get; init; }

    public Gender? Gender { get; init; }

    // ?? Optional address ?????????????????????????????????????????????????????

    [MaxLength(250)]
    public string? Address { get; init; }

    [MaxLength(100)]
    public string? City { get; init; }

    [MaxLength(100)]
    public string? State { get; init; }

    [MaxLength(100)]
    public string? Country { get; init; }

    [MaxLength(20)]
    public string? Pincode { get; init; }

    // ?? MANDATORY safety fields ???????????????????????????????????????????????

    /// <summary>Required. Must be 7ñ15 digits.</summary>
    [Required(ErrorMessage = "MobileNumber is required.")]
    [RegularExpression(@"^\d{7,15}$", ErrorMessage = "MobileNumber must be 7ñ15 digits with no spaces or symbols.")]
    public string MobileNumber { get; init; } = string.Empty;

    /// <summary>Required. Must be a past date; user must be at least 16 years old.</summary>
    [Required(ErrorMessage = "DateOfBirth is required.")]
    public DateTime DateOfBirth { get; init; }

    /// <summary>Required for medical emergencies.</summary>
    [Required(ErrorMessage = "BloodGroup is required.")]
    public BloodGroup BloodGroup { get; init; }

    /// <summary>Required for emergencies.</summary>
    [Required(ErrorMessage = "EmergencyContactName is required.")]
    [MaxLength(100, ErrorMessage = "EmergencyContactName must not exceed 100 characters.")]
    public string EmergencyContactName { get; init; } = string.Empty;

    /// <summary>Required. Must be 7ñ15 digits.</summary>
    [Required(ErrorMessage = "EmergencyContactNumber is required.")]
    [RegularExpression(@"^\d{7,15}$", ErrorMessage = "EmergencyContactNumber must be 7ñ15 digits with no spaces or symbols.")]
    public string EmergencyContactNumber { get; init; } = string.Empty;

    /// <summary>Required (e.g. Father, Spouse, Friend).</summary>
    [Required(ErrorMessage = "EmergencyContactRelation is required.")]
    [MaxLength(50, ErrorMessage = "EmergencyContactRelation must not exceed 50 characters.")]
    public string EmergencyContactRelation { get; init; } = string.Empty;

    // ?? Optional medical ?????????????????????????????????????????????????????

    [MaxLength(500)]
    public string? MedicalConditions { get; init; }

    [MaxLength(500)]
    public string? Allergies { get; init; }

    // ?? Optional insurance ????????????????????????????????????????????????????

    [MaxLength(100)]
    public string? InsuranceProvider { get; init; }

    [MaxLength(100)]
    public string? InsurancePolicyNumber { get; init; }

    // ?? Optional preferred services ???????????????????????????????????????????

    [MaxLength(100)]
    public string? PreferredMechanicName { get; init; }

    [RegularExpression(@"^\d{7,15}$", ErrorMessage = "PreferredMechanicContact must be 7ñ15 digits.")]
    public string? PreferredMechanicContact { get; init; }

    [MaxLength(150)]
    public string? PreferredHospital { get; init; }

    [RegularExpression(@"^\d{7,15}$", ErrorMessage = "PreferredHospitalContact must be 7ñ15 digits.")]
    public string? PreferredHospitalContact { get; init; }

    // ?? Optional license ??????????????????????????????????????????????????????

    [MaxLength(50)]
    public string? DrivingLicenseNumber { get; init; }

    public DateTime? LicenseExpiryDate { get; init; }

    // ?? Optional notes ????????????????????????????????????????????????????????

    [MaxLength(1000)]
    public string? RiderNotes { get; init; }
}

